export function computeMinistryPerformance(recs: Recommendation[]): MinistryPerformance[] {
  const map = new Map<string, Recommendation[]>();
  recs.forEach((r) => {
    const key = r.primaryMinistry;
    if (!map.has(key)) map.set(key, []);
    map.get(key)!.push(r);
  });

  const aiRemarks: Record<string, string> = {
    "Ministry of Environment, Forest and Climate Change": "Strong closure discipline; documentary completion through gazette notification.",
    "Ministry of Commerce and Industry / DPIIT": "Higher risk due to pending inter-ministerial consultations and Cabinet Note dependencies.",
    "Ministry of Micro, Small and Medium Enterprises": "Moderate momentum; multiple reforms bundled into single Amendment Bill creating dependency risk.",
    "Ministry of Corporate Affairs": "Mixed progress; rule amendments completed but Act amendments await parliamentary calendar.",
    "Ministry of Textiles": "Early stage; stakeholder consultations ongoing. On track for timeline.",
    "Ministry of Chemicals and Fertilizers": "No action initiated; inter-departmental clearance pending. Requires escalation.",
    "Ministry of Steel": "Partial implementation achieved; remaining items blocked by BIS infrastructure readiness.",
  };

  return Array.from(map.entries()).map(([ministry, items]) => ({
    ministry,
    total: items.length,
    fullyImplemented: items.filter((r) => r.implementationStatus === "Fully implemented").length,
    partiallyImplemented: items.filter((r) => r.implementationStatus === "Partially implemented").length,
    underProgress: items.filter((r) => r.implementationStatus === "Under progress").length,
    yetToInitiate: items.filter((r) => r.implementationStatus === "Yet to initiate").length,
    noAction: items.filter((r) => r.implementationStatus === "No action").length,
    overdueCount: items.filter((r) => r.delayDays > 0).length,
    avgDelay: Math.round(items.reduce((s, r) => s + r.delayDays, 0) / items.length),
    riskRating: items.some((r) => r.riskLevel === "High") ? "High" : items.some((r) => r.riskLevel === "Medium") ? "Medium" : "Low",
    aiRemark: aiRemarks[ministry] || "Performance data under analysis.",
    pendingWithDirector: items.filter((r) => r.currentOwner === "Ministry Director").length,
    pendingWithApprover: items.filter((r) => r.currentOwner === "Ministry Approver").length,
    pendingWithNITI: items.filter((r) => r.currentOwner === "NITI Approver").length,
  }));
}

